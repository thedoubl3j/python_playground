#list practice. helping learn cardinal vs ordinal lists
animals = ['bear', 'python3.6', 'peacock', 'kangaroo', 'whale', 'platypus']

#the animal at 1
animals[1] = python
#the third animal
animals[2] = peacock
#the first animal
animals[0] = Bear
#the animal at 3
animals[3] = kangaroo
#the fifth animal
animals[4] = whale
#the animal at 2
print(peacock)
#the sixth animal
animals[5] = platypus
#the animal at 4
print(whale)
